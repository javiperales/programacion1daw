
public class Ingredientes {
	String nombreIngredientes;
	private Float precioGramo;
	public Ingredientes(String nombreIngredientes, Float precioGramo) {
		super();
		this.nombreIngredientes = nombreIngredientes;
		this.precioGramo = precioGramo;
	}
	public String getNombreIngredientes() {
		return nombreIngredientes;
	}
	public void setNombreIngredientes(String nombreIngredientes) {
		this.nombreIngredientes = nombreIngredientes;
	}
	public Float getPrecioGramo() {
		return precioGramo;
	}
	public void setPrecioGramo(Float precioGramo) {
		this.precioGramo = precioGramo;
	}
	
	
			
	
	
	
}
